public class Motor{
    private int qtdPist = 0;
    private int potencia = 0;

    //setters
    public void setQtdPist(int qtdPist) {
        this.qtdPist = qtdPist;
    }

    public void setPotencia(int potencia) {
        this.potencia = potencia;
    }


    //getters
    public int getQtdPist() {
        return qtdPist;
    }

    public int getPotencia() {
        return potencia;
    }
}
